function save_results_seg(true_positive, true_negative, false_positive, false_negative, precision, recall, path)
    fileID = fopen([path,'results_segmentation.txt'],'w');
    str = ['True Positive: ', num2str(true_negative),...
           '\r\nTrue Negative: ',num2str(true_negative),...
           '\r\nFalse Positive: ',num2str(false_positive),...
           '\r\nFalse Negative: ',num2str(false_negative),...
           '\r\nPrecision: ',num2str(precision),...
           '\r\nRecall: ',num2str(recall)];

    fprintf(fileID,str);
    fclose(fileID);
end